import turtle
t = turtle.Turtle()
t.penup()
t.goto(0, -100)
t.pendown()
t.begin_fill()
t.color('yellow')
t.circle(100)
t.end_fill()
t.penup()
t.goto(-40, 30)
t.pendown()
t.begin_fill()
t.color('black')
t.circle(20)
t.end_fill()
t.penup()
t.goto(40, 30)
t.pendown()
t.begin_fill()
t.color('black')
t.circle(20)
t.end_fill()
t.penup()
t.goto(-60, -20)
t.pendown()
t.pensize(10)
t.setheading(-60)
t.circle(80, 120)
turtle.done()
